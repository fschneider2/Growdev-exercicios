# Escreva um programa que receba 3 números, faça a soma dos dois primeiros e 
# depois divida o resultado pelo 3º número. Após, exiba o resultado na tela..
num_1 = int(input('Informe o primeiro numero: '))
num_2 = int(input('Informe o segundo numero: '))
num_3 = int(input('Informe o terceiro numero: '))
formula = (num_1 + num_2) / num_3
print('O resultado da equação é: ' + str(formula))
